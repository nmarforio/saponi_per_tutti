import Head from "next/head";
import { Inter } from "next/font/google";
import { useEffect, useState } from "react";
import Quantity from "@/components/Quantity";
import { Swiper, SwiperSlide } from "swiper/react";
import "swiper/css";
import "swiper/css/navigation";
import "swiper/css/effect-fade";
import { Navigation, EffectFade } from "swiper";
import Image from "next/image";
import { useRouter } from "next/router";
import StarRating from "react-rating-stars-component";

const inter = Inter({ subsets: ["latin"] });

export default function Home() {
  const [soapList, setSoapList] = useState([]);
  const [moreInfos, setMoreInfos] = useState(false);
  const [rating, setRating] = useState(0);
  const router = useRouter();

  const customStyles = {
    overlay: {
      backgroundColor: "rgba(0, 0, 0, 0.6)",
    },
    content: {
      top: "50%",
      left: "50%",
      right: "auto",
      bottom: "auto",
      marginRight: "-50%",
      transform: "translate(-50%, -50%)",
    },
  };
  const handleStarClick = (nextValue, prevValue, name) => {};

  useEffect(() => {
    const fetchData = async () => {
      const data = await fetch("/api/soaps/");
      const json = await data.json();

      setSoapList(json);
    };
    fetchData().catch(console.error);
  }, []);

  return (
    <>
      <Head>
        <title>Saponi-per-tutti</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Swiper
        modules={[Navigation, EffectFade]}
        navigation
        effect
        speed={800}
        slidesPerView={1}
        loop
        className="myswiper"
      >
        {soapList.map((soap, index) => {
          return (
            <>
              <SwiperSlide key={soap._id}>
                <div key={soap._id} className="soapdetails">
                  <h1>{soap.name}</h1>
                  <div className="imgDiv">
                    <Image
                      key={soap._id}
                      alt={soap._id}
                      src={soap.image}
                      width={200}
                      height={250}
                    />
                  </div>
                  <Quantity soapId={soap._id} index={index} />
                  <strong>CHF: {soap.price}</strong>
                  <button
                    className="menuButton"
                    onClick={() => setMoreInfos(!moreInfos)}
                  >
                    <Image
                      src={"/dots.png"}
                      alt={"menu"}
                      width={30}
                      height={30}
                    />
                  </button>
                  <div className="ratingStar">
                    <StarRating
                      value={rating}
                      onStarClick={(nextValue, prevValue, name) =>
                        handleStarClick(nextValue, prevValue, name)
                      }
                      activeColor="#dc9109"
                    />
                  </div>
                  <div className={moreInfos ? "toogleInfo" : "toogleInfoNone"}>
                    <p>{soap.description}</p>
                    <strong>Sostanze</strong>
                    <p className="recipes">{soap.recipes}</p>
                  </div>
                </div>
              </SwiperSlide>
            </>
          );
        })}
      </Swiper>
    </>
  );
}
