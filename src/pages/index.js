import Head from "next/head";
import { Inter } from "next/font/google";
import { useEffect, useState } from "react";
import Quantity from "@/components/Quantity";
import { Swiper, SwiperSlide } from "swiper/react";
import "swiper/css";
import "swiper/css/navigation";
import "swiper/css/effect-fade";
import { Navigation, EffectFade } from "swiper";
import Image from "next/image";
import { useRouter } from "next/router";
import styles from "@/styles/styles";
import { clsx } from "clsx";

const inter = Inter({ subsets: ["latin"] });

export default function Home() {
  const [soapList, setSoapList] = useState([]);
  const [moreInfos, setMoreInfos] = useState(false);
  const router = useRouter();

  useEffect(() => {
    const fetchData = async () => {
      const data = await fetch("/api/soaps/");
      const json = await data.json();

      setSoapList(json);
    };
    fetchData().catch(console.error);
  }, []);

  console.log("image", moreInfos);

  return (
    <>
      <Head>
        <title>Saponi-per-tutti</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Swiper
        modules={[Navigation, EffectFade]}
        navigation
        effect
        speed={800}
        slidesPerView={1}
        loop
        className="myswiper"
      >
        {soapList.map((soap, index) => {
          return (
            <>
              <SwiperSlide key={soap._id}>
                <div key={soap._id} className="soapdetails">
                  <h1>{soap.name}</h1>
                  <div className="imgDiv">
                    <Image
                      key={soap._id}
                      alt={soap._id}
                      src={soap.image}
                      width={200}
                      height={250}
                    />
                  </div>
                  <Quantity soapId={soap._id} index={index} />
                  <strong>CHF: {soap.price}</strong>
                  <button onClick={() => setMoreInfos(!moreInfos)}>
                    altro da sapere
                  </button>
                  <div className={moreInfos ? "toogleInfo" : "toogleInfoNone"}>
                    <p>{soap.description}</p>
                    <strong>Sostanze</strong>
                    <p className="recipes">{soap.recipes}</p>
                  </div>
                </div>
              </SwiperSlide>
            </>
          );
        })}
      </Swiper>
    </>
  );
}

