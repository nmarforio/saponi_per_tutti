import Head from "next/head";
import { Inter } from "next/font/google";
import { useEffect, useState } from "react";
import Quantity from "@/components/Quantity";
import { Swiper, SwiperSlide } from "swiper/react";
import "swiper/css";
import "swiper/css/navigation";
import "swiper/css/effect-fade";
import { Navigation, EffectFade } from "swiper";
import Image from "next/image";

const inter = Inter({ subsets: ["latin"] });

export default function Home() {
  const [soapList, setSoapList] = useState([]);

  useEffect(() => {
    const fetchData = async () => {
      const data = await fetch("/api/soaps/");
      const json = await data.json();

      setSoapList(json);
    };
    fetchData().catch(console.error);
  }, []);

  return (
    <>
      <Head>
        <title>Saponi-per-tutti</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Swiper
        modules={[Navigation, EffectFade]}
        navigation
        effect
        speed={800}
        slidesPerView={1}
        loop
        className="myswiper"
      >
        {soapList.map((soap, index) => {
          return (
            <SwiperSlide key={soap._id}>
              <div key={soap._id} className="soapdetails">
                <h1>{soap.name}</h1>
                <Image
                  key={soap._id}
                  alt={soap._id}
                  src={soap.image[index]}
                  width={200}
                  height={200}
                  style={{ objectFit: "cover" }}
                />
                <Quantity soapId={soap._id} index={index} />
                <p>CHF: {soap.price}</p>
                <p>{soap.description}</p>
                <h4>Sostanze</h4>
                <p className="recipes">{soap.recipes}</p>;
              </div>
            </SwiperSlide>
          );
        })}
      </Swiper>
    </>
  );
}
